using System;
using System.Collections.Generic;
using System.Linq;
using System.Text;
using System.Threading.Tasks;

namespace MethodExercise
{
    class operations
    {
        public static int add (int a, int b)
        {
            int c = a + b;
            return c;
        }

        public static int multiply(int a, int b)
        {
            int c = a * b;
            return c;
        }
        public static int divide(int a, int b)
        {
            int c = a / b;
            return c;
        }
    }
}
